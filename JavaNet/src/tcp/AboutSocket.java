package tcp;

public class AboutSocket {
	/*
	 * Socket(套接字)实际上是TCP的抽象,应用层通过
	 * Socket从传输层拿到传输的网络数据
	 * 
	 * Socket有如下原语:
	 * SOCKET:新建一个socket
	 * BIND:socket与ip地址,端口号绑定
	 * LISTEN:通告愿意接受连接,并给出队列大小
	 * ACCEPT:阻塞,直到有人企图进行连接
	 * CONNECT:主动尝试建立连接
	 * SEND:发送数据
	 * RECV:接收数据
	 * CLOSE:关闭socket
	 */

}
